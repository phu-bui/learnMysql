    private void showBDL(){
        tableDiem.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {

            },
            new String [] {
                "Mã sinh viên", "Họ", "Tên", "Ngày sinh", "Nơi sinh", "Điểm"
            }
        ));
        Map<Integer,Object> map = commandOther.getListDiemLop(this.txfMaLoppn52.getText());
        ArrayList<SinhVien> listSV = new ArrayList<>();
        ArrayList<SinhVienLop> listSVL = new ArrayList<>();
        for(Map.Entry<Integer,Object> entry : map.entrySet())
        {
            if(entry.getKey()==1) listSV = (ArrayList<SinhVien>)entry.getValue();
            if(entry.getKey()==2) listSVL = (ArrayList<SinhVienLop>)entry.getValue();
        }
        DefaultTableModel model = (DefaultTableModel)tableDiem.getModel();
        model.setRowCount(0);
        Object[] row = new Object[6];
        for(int i=0;i<listSVL.size();i++)
        {            
            row[0]=listSV.get(i).getMaSV();
            row[1]=listSV.get(i).getHoSV();
            row[2]=listSV.get(i).getTenSV();
            row[3]=listSV.get(i).getNgaySinh();
            row[4]=listSV.get(i).getNoiSinh();
            row[5]=listSVL.get(i).getDiem();
            model.addRow(row);
        }
    }
    
