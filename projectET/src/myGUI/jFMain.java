    private void jMIInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMIInputActionPerformed
        // TODO add your handling code here:
        JFileChooser c= new JFileChooser();
        c.setCurrentDirectory(new java.io.File("."));
        c.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        c.setAcceptAllFileFilterUsed(false);
        int rVal = c.showOpenDialog(null);
        if (rVal == JFileChooser.APPROVE_OPTION)
        {
            String filename = c.getSelectedFile().getName();
            String dir = c.getCurrentDirectory().toString();
            String filePath = dir + "/" + filename;
            try {
                commandMonHoc.inputMH(filePath+"/MonHoc.csv");
                commandGiaoVien.inputGV(filePath+"/GiaoVien.csv");
                commandSinhVien.inputSV(filePath+"/SinhVien.csv");
                cmmandLop.inputLop(filePath+"/Lop.csv");
                commandSinhVienLop.inputSVL(filePath+"/SinhVienLop.csv");
                showMH();
                showGV();
                showSV();
                showLop();
            } catch (IOException ex) {
                Logger.getLogger(jFMain.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMIInputActionPerformed

