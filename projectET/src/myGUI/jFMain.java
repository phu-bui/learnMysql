    private void showMH()
    {
        ArrayList<MonHoc> list = commandMonHoc.getListMH();
        DefaultTableModel model = (DefaultTableModel)tableMH.getModel();
        model.setRowCount(0);
        Object[] row = new Object[3];
        for(int i=0;i<list.size();i++)
        {            
            row[0]=list.get(i).getMaMH();
            row[1]=list.get(i).getTenMH();
            row[2]=list.get(i).getSoTC();
            model.addRow(row);
        }
    }
    
